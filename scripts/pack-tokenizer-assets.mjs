import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { brotliCompressSync, constants as zlibConstants } from 'node:zlib';
import o200kBase from 'js-tiktoken/ranks/o200k_base';
import qwen35Asset from '../node_modules/@cyberlangke/tokkit-qwen/dist/generated/qwen3_5.js';
import deepseek31Asset from '../node_modules/@cyberlangke/tokkit-deepseek/dist/generated/deepseek_v3_1.js';

const BASE85_ALPHABET =
	'0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz!#$%&()*+-;<=>?@^_`{|}~';

function encodeBase85(bytes) {
	let output = '';
	for (let index = 0; index < bytes.length; index += 4) {
		let value =
			((bytes[index] ?? 0) * 2 ** 24) +
			((bytes[index + 1] ?? 0) << 16) +
			((bytes[index + 2] ?? 0) << 8) +
			(bytes[index + 3] ?? 0);
		const chars = new Array(5);
		for (let digit = 4; digit >= 0; digit -= 1) {
			chars[digit] = BASE85_ALPHABET[value % 85];
			value = Math.floor(value / 85);
		}
		output += chars.join('');
	}
	return output;
}

function chunkString(value, size = 12000) {
	const chunks = [];
	for (let index = 0; index < value.length; index += size) {
		chunks.push(value.slice(index, index + size));
	}
	return chunks;
}

async function writePackedModule(fileName, exportName, bytes) {
	const encoded = encodeBase85(bytes);
	const filePath = resolve('src/generated', fileName);
	const chunks = chunkString(encoded)
		.map((chunk) => `\t${JSON.stringify(chunk)},`)
		.join('\n');
	const contents = `// Generated by scripts/pack-tokenizer-assets.mjs.\n// Stores tokenizer data in base85 to reduce the released main.js size.\n\nexport const ${exportName}Length = ${bytes.length};\nexport const ${exportName} = [\n${chunks}\n].join('');\n`;

	await mkdir(dirname(filePath), { recursive: true });
	await writeFile(filePath, contents, 'utf8');
}

const brotliOptions = {
	params: {
		[zlibConstants.BROTLI_PARAM_QUALITY]: 11,
	},
};

await writePackedModule(
	'o200k-base-packed.ts',
	'o200kBasePacked',
	brotliCompressSync(Buffer.from(JSON.stringify(o200kBase), 'utf8'), brotliOptions),
);
await writePackedModule(
	'qwen35-packed.ts',
	'qwen35Packed',
	Buffer.from(qwen35Asset, 'base64'),
);
await writePackedModule(
	'deepseek31-packed.ts',
	'deepseek31Packed',
	Buffer.from(deepseek31Asset, 'base64'),
);
